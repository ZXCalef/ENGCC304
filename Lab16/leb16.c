#include <stdio.h>    // ใช้สำหรับ Input / Output

// ---------------- ฟังก์ชันหลัก ----------------
int main ( ) {
    // ประกาศอาเรย์ตามที่โจทย์กำหนด
    int arr[] = { 15 , 7 , 25 , 3 , 73 , 32 , 45 } ;
    int n = sizeof( arr ) / sizeof( arr[ 0 ] ) ; // หาขนาดของอาเรย์
    int i , j , temp , pos = -1 ;                // ตัวแปรช่วยในการวนลูปและเก็บตำแหน่ง

    // ---------- แสดงอาเรย์เก่า ----------
    printf ( "Old Series : " ) ;                 // แสดงข้อความ "Old Series :"
    for ( i = 0 ; i < n ; i++ ) {
        printf ( "%d" , arr[ i ] ) ;            // พิมพ์ค่าของอาเรย์แต่ละตัว
        if ( i < n - 1 ) printf ( ", " ) ;      // ถ้าไม่ใช่ตัวสุดท้ายให้เว้นด้วยเครื่องหมาย ,
    }
    printf ( "\n" ) ;                            // ขึ้นบรรทัดใหม่

    // ---------- จัดเรียงอาเรย์จากน้อยไปมาก (Bubble Sort) ----------
    for ( i = 0 ; i < n - 1 ; i++ ) {           // วนลูป n-1 รอบ
        for ( j = 0 ; j < n - i - 1 ; j++ ) {   // วนลูปเช็คคู่ข้างเคียง
            if ( arr[ j ] > arr[ j + 1 ] ) {   // ถ้าตัวก่อนมากกว่าตัวหลัง
                temp = arr[ j ] ;              // สลับตำแหน่ง
                arr[ j ] = arr[ j + 1 ] ;
                arr[ j + 1 ] = temp ;
            }
        }
    }

    // ---------- แสดงอาเรย์ใหม่หลังการจัดเรียง ----------
    printf ( "New Series : " ) ;                 // แสดงข้อความ "New Series :"
    for ( i = 0 ; i < n ; i++ ) {
        printf ( "%d" , arr[ i ] ) ;            // พิมพ์ค่าของอาเรย์แต่ละตัว
        if ( i < n - 1 ) printf ( ", " ) ;      // ถ้าไม่ใช่ตัวสุดท้ายให้เว้นด้วยเครื่องหมาย ,
    }
    printf ( "\n" ) ;                            // ขึ้นบรรทัดใหม่

    // ---------- หาตำแหน่งของเลข 32 ----------
    for ( i = 0 ; i < n ; i++ ) {               // วนลูปตรวจสอบทุกตัวในอาเรย์
        if ( arr[ i ] == 32 ) {                 // ถ้าพบเลข 32
            pos = i ;                           // เก็บตำแหน่ง
            break ;                             // ออกจากลูปทันที
        }
    }

    // ---------- แสดงผลตำแหน่งของเลข 32 ----------
    if ( pos != -1 )                              // ถ้าพบเลข 32
        printf ( "Pos of 32 : %d\n" , pos ) ;    // แสดงตำแหน่ง (นับจาก 0)

    return 0 ;                                    // จบการทำงานโปรแกรม
}